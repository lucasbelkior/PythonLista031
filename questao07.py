'''
7)	Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

num1 = float(input("Informe um numero:"))
num2 = float(input("Informe outro numero:"))
num3 = float(input("Informe mais um numero:"))
mediar = (num1+num2+num3)/3
print("A media aritmetica desses 3 numeros sera:",mediar)


